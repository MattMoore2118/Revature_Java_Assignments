package eg1;

public class Switch_Case_Demo {
	
	public static void main(String[] args) {
		int day=2;
		switch (day) {
		case 1:System.out.println("Its Monday");
			break;
		case 2:System.out.println("Its Tuesday");
			break;
		default:System.out.println("There is no matching day");
			break;
		}
		
	}

}
